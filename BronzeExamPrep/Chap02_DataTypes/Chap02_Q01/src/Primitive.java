public class Primitive{
    public static void main(String[] args) {
        double dnum = 2.5;
        byte bnum = 1;
        System.out.println("dnum = " + dnum);
        System.out.println("bnum = " + bnum);
        System.out.println("IntegerとNumberは整数を扱うクラスです。");
        System.out.println("Characterは文字を扱うクラスです。");
        System.out.println("Integer・Number・Characterは参照型です。");
    }
}